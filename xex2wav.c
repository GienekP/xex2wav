void putInt32(U8 *data, unsigned int value)
{
	unsigned int a,b,c,d;
	a=(value & 0xFF);
	value>>=8;
	b=(value & 0xFF);
	value>>=8;
	c=(value & 0xFF);
	value>>=8;
	d=(value & 0xFF);
	data[0]=(U8)(a);
	data[1]=(U8)(b);
	data[2]=(U8)(c);
	data[3]=(U8)(d);	
}
