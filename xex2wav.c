/*--------------------------------------------------------------------*/
void addSpace(int *track, unsigned int *pos)
{

	unsigned int i,j;
	if (pos)
	{
		for (j=0; j<NOSPACE; j++)
		{
			if (track)
			{
				for (i=0; i<SIZESPACE; i++) {track[(*pos)+i]=space[i];};
			};
			*pos+=SIZESPACE;
		};
	};
}
/*--------------------------------------------------------------------*/

void addSign(int *track, unsigned int *pos)
{

	unsigned int i,j;
	if (pos)
	{
		for (j=0; j<NOSIGN; j++)
		{
			if (track)
			{
				for (i=0; i<SIZESIGN; i++) {track[(*pos)+i]=sign[i];};
			};
			*pos+=SIZESIGN;
		};
	};
}
/*--------------------------------------------------------------------*/
void addByte(int *track, unsigned int *pos, U8 byte)
{
	unsigned int i; 
	addSpace(track,pos);
	for (i=0; i<8; i++)
	{
		U8 bit=((byte>>i) & 1);
		if (bit==0) {addSpace(track,pos);} else {addSign(track,pos);};
	};
	addSign(track,pos);
}
/*--------------------------------------------------------------------*/
void addRecord(int *track, unsigned int *pos, const U8 *byte, U8 size)
{
	U8 i;
	addByte(track,pos,0x55);
	addByte(track,pos,0x55);
	if (size==0)
	{
		addByte(track,pos,0xFE);
		for (i=0; i<128; i++) {addByte(track,pos,0x00);};
		addByte(track,pos,0xA9);
	}
	else if (size==128)
	{
		unsigned int sum=0xA7;
		addByte(track,pos,0xFC);
		for (i=0; i<128; i++)
		{
			addByte(track,pos,byte[i]);
			sum+=byte[i];
			if (sum>0xFF)
			{
				sum&=0xFF;
				sum++;
			};
		};
		addByte(track,pos,(sum & 0xFF));
	}
	else
	{
		unsigned int sum=0xA5;
		addByte(track,pos,0xFA);
		for (i=0; i<size; i++)
		{
			addByte(track,pos,byte[i]);
			sum+=byte[i];
			if (sum>0xFF)
			{
				sum&=0xFF;
				sum++;
			};
		};
		for (i=size; i<127; i++) {addByte(track,pos,0x00);};
		addByte(track,pos,size);
		sum+=size;
		if (sum>0xFF)
		{
			sum&=0xFF;
			sum++;
		};				
		addByte(track,pos,(sum & 0xFF));
	};
}
/*--------------------------------------------------------------------*/
void addGap(int *track, unsigned int *pos, U8 mode)
{
	unsigned int i,ms,n;
	switch (mode)
	{
		case 1: {ms=250;} break;
		case 2: {ms=3000;} break;
		case 3: {ms=20*1000;} break;		
		default: {ms=0;} break;
	};
	n=(SF*ms)/(1000*NOSIGN*SIZESIGN);
	for (i=0; i<n; i++) {addSign(track,pos);};
}
/*--------------------------------------------------------------------*/
void addSilent(int *track, unsigned int *pos)
{
	unsigned int i;
	if (pos)
	{
		if (track)
		{
			for (i=0; i<SF; i++) {track[(*pos)+i]=0;};
		};
		(*pos)+=SF;
	};
}
/*--------------------------------------------------------------------*/
void addFile(int *track, unsigned int *pos, const U8 *file, unsigned int size, U8 xex)
{
	unsigned int m=0;
	int cnt=size;
	addGap(track,pos,3);
	while (cnt>0)
	{
		addGap(track,pos,1);
		if (cnt>128)
		{
			addRecord(track,pos,&file[m],128);
			m+=128;
			cnt-=128;
		}
		else
		{
			addRecord(track,pos,&file[m],cnt);
			cnt-=128;
		};
	};
	addRecord(track,pos,file,0);
}
/*--------------------------------------------------------------------*/
void addXEX(int *track, unsigned int *pos, const U8 *xex, unsigned int size)
{
	addFile(track,pos,exma,sizeof(exma),0);
	addFile(track,pos,xex,size,1);
	addSilent(track,pos);
}
